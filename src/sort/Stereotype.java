public class Stereotype{
	public int sid;
	public String stereotypeName;

	public Stereotype(int sid, String stereotypeName){
		this.sid = sid;
	}

	public void setSid(int sid){
		this.sid = sid;
	}

	public int getSid(){
		return this.sid;
	}

	public void setName(String stereotypeName){
		this.stereotypeName = stereotypeName;
	}
	
}
